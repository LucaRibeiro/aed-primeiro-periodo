public class Q07 {
    /**
     * Retorna o decremento de i
     * @param i inteiro para decremento
     * @return retorna o decremento do parâmetro i
     */
    int m1(int i){
        return i--;
    }

    /**
     * Retorna i e depois o decrementa
     * @param i inteiro para decremento
     * @return retona o valor do parâmetro i
     */
    int m2(int i){
        return --i;
    }
}
